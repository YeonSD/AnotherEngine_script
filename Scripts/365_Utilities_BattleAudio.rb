  if !ret
    # Check global metadata
    music = GameData::Metadata.get.wild_victory_BGM
    ret = pbStringToAudioFile(music) if music && music != ""
  end
  ret = pbStringToAudioFile("Battle victory") if !ret
  ret.name = "../../Audio/BGM/" + ret.name
  return ret
end

