alias __random_item_original_pbItemBall pbItemBall

def pbItemBall(item, quantity = 1)
  # [ВѕўВаЋ ьЈгВЮИьіИ] В┤ѕЖИ░Ж░њВЮё falseВЌљВёю trueвАю в│ђЖ▓йьЋўВЌг "ЖИ░в│И В╝юВДљ" ВЃЂьЃювАю вДївЊГвІѕвІц.
  $random_item_switch = true if $random_item_switch.nil? 

  if $random_item_switch
    item_data = GameData::Item.get(item)
    
    # ЖИѕВДђ вфЕвАЮ
    excluded_list = [:REDCONTROLLER, :SHINYCANDY, :ADAPTIVELEVELCANDY]
    
    if !excluded_list.include?(item) && !item_data.is_key_item?
      if item_data.is_TM?
        all_tms = GameData::Item.keys.select { |i| GameData::Item.get(i).is_TM? }
        item = all_tms.sample unless all_tms.empty?
      else
        item = RandomItemSystem.get_random_item
      end
    end
  end
  
  __random_item_original_pbItemBall(item, quantity)
end

