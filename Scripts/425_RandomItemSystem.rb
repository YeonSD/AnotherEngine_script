module RandomItemSystem
  CHANCE_BATTLE  = 60
  CHANCE_Z_ITEM  = 15
  CHANCE_MEGA    = 15
  CHANCE_CANDY   = 5
  CHANCE_CAPS    = 5

  def self.valid(list)
    list.select { |id| GameData::Item.exists?(id) }
  end

  def self.get_random_item
    battle  = valid(RandomItemPool::BATTLE_ITEMS)
    zitems  = valid(RandomItemPool::Z_CRYSTALS)
    megas   = valid(RandomItemPool::MEGA_STONES)
    candies = valid(RandomItemPool::CANDIES)
    caps    = valid(RandomItemPool::BOTTLECAPS)

    roll = rand(100)
    if roll < CHANCE_BATTLE
      return battle.sample || :POTION
    elsif roll < CHANCE_BATTLE + CHANCE_Z_ITEM
      return zitems.sample || :POTION
    elsif roll < CHANCE_BATTLE + CHANCE_Z_ITEM + CHANCE_MEGA
      return megas.sample || :POTION
    elsif roll < CHANCE_BATTLE + CHANCE_Z_ITEM + CHANCE_MEGA + CHANCE_CANDY
      return candies.sample || :POTION
    else
      return caps.sample || :POTION
    end
  end
end